public class Patternprog {

	   public static void main(String[] args) 

	    {
		   int n=5;//5 Star print
		   for (int i=1;i<=n;i++) {//1to5 star coloum
			   
		   
				    for (int j=1;j<=n;j++ ) {// 1 to 5 star Rows
				    	System.out.print("*");
				    }
				    System.out.println();
				    
}
	   }
	   
}
	   
	   
	   
	   
	   
	   
	   
	   